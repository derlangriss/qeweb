                            <p>สวนพฤกษศาสตร์สมเด็จพระนางเจ้าสิริกิติ์ มีกิจกรรมแหล่งเรียนรู้ด้านแมลง ได้ทำการสำรวจแมลงปอ 2 แหล่ง คือ พื้นที่บริเวณเรือนกระจก และ น้ำตกแม่สาน้อยในสวนพฤกษศาสตร์ฯ ซึ่งเป็นสถานที่ที่มีความอุดมสมบูรณ์ทางธรรมชาติ จึงมักจะพบตัวเต็มวัยของแมลงปออาศัยบริเวณแหล่งน้ำทั้งสอง โดย พื้นที่บริเวณเรือนกระจก เป็นพื้นที่ที่เป็นแหล่งน้ำนิ่ง มักจะพบแมลงปอบ้าน แมลงปอยักษ์ แมลงปอเสือ ส่วนบริเวณน้ำตกแม่สาน้อย เป็นที่ี่ที่เป็นแหล่งน้ำไหล จึงมักจะพบแมลงปอเข็มและแมลงปอน้ำตก
                            </p>
                            <h1>แมลงปอในสวนพฤกษศาสตร์สมเด็จพระนางเจ้าสิริกิติ์
                            </h1>
                            <p>ในประเทศไทย มีการค้นพบแมลงปอแล้ว 19 วงศ์ 321 ชนิดและจากการสำรวจแมลงปอที่พบในสวนพฤกษศาสตร์ฯ ในปัจจุบันพบ 10 วงศ์ 29 สกุล 39 ชนิด โดยในที่นี้จะจัดจำแนกแมลงปอตามอันดับย่อย (Suborder)โดยจำแนกจากส่วนตารวม (compound eyes) ลักษณะปีก การเกาะพักของแมลงปอแบ่งเป็น 2 กลุ่ม คือ
                            </p>
                            <p>1. suborder Anisoptera โคนปีกคู่หลังจะกว้างกว่าโคนปีกคู่หน้า ตารวม (compound eyes) จะอยู่ใกล้กัน และในขณะเกาะพักจะกางปีกราบกับพื้น ได้แก่ แมลงปอปีกราบ (Dragonflies) มี 22 ชนิด 17 สกุล จาก 3 วงศ์
                            </p>
