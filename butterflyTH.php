               <h1>ขออารัมภบทสักนิดนะคะ</h1>
<p><img src="assets/images/articles/butterflyinqsbg/th/lifecircle.png" alt="images" width="350" class="pull-right img-align-right">นักแต่งกวีชาวฝรั่งเศส ขื่อ Ponce Denis Ecouchard Lebrun บรรยายตัวฉันและเพื่อน ๆ ผีเสื้อของฉันว่า เป็น  “ดอกไม้บินได้” ที่นี่เป็นสวนพฤกษศาสตร์ที่มี “ดอกไม้ที่ถูกตรึงไว้กับพื้น” มากมาย แต่ฉันกำลังจะเล่าเรื่องราวที่เกี่ยวกับตัวฉัน และเพื่อนๆ ของฉันที่บินได้ให้คุณได้ทราบก่อนค่ะ
</p>

<p>ผีเสื้อมักจะได้รับความชื่นชมในเรื่องของความสวยงาม ความบอบบาง และความสง่างาม แต่เรื่องที่เกี่ยวกับเรามีมากกว่านั้นอีกนะ  ผีเสื้อ เป็นเพียงแมลงกลุ่มเล็ก ๆ กลุ่มหนึ่งในหมู่แมลงที่มีแต่ความมหัศจรรย์ แมลงหลายจำพวกมีประวัติชีวิต และพฤติกรรม ที่น่าตื่นตาตื่นใจ นอกเหนือไปจากความงามของพวกเขา หนึ่งในสิ่งที่มหัศจรรย์ที่สุดเกี่ยวกับผีเสื้อที่เชื่อว่าทุกคนสามารถเห็นได้ คือวงจรชีวิตของพวกเรา และสิ่งสำคัญที่สุดที่คุณควรรู้คือ ฉันสามารถเปลี่ยนรูปร่างจากที่เห็นในปัจจุบัน เป็นผีเสื้อ และบินเข้ารวมกลุ่มกับเพื่อน ๆ ที่เป็นดอกไม้ที่บินได้อยู่ก่อนแล้ว ผีเสื้อและแมลงอื่น ๆ อีกมากมายมีวงจรชีวิตที่น่าทึ่งที่เปลี่ยนจากไข่ (Egg) ธรรมดาๆ ไปเป็นหนอน (Larva) ที่เปรียบได้กับเครื่องจักรที่กินอาหารได้ก่อนที่จะเปลี่ยนเป็นดักแด้อาศัยอยู่ภายในกะเปาะ (Chrysalis) และเปลี่ยนแปลงอย่างสมบูรณ์แบบอีกครั้ง เพื่อเป็นผีเสื้อ (Adult) - ซึ่งไม่มีรูปลักษณ์อะไรที่จะบอกได้เลยว่าเมื่อไม่กี่วันก่อนหน้านั้น เคยมีลักษณะเป็นตัวหนอนมาก่อน</p>

