<div class="clear"></div>
<p>&nbsp;</p>

<p>While you are at QSBG you may also meet some of my friends from “the darkside” — the moths! They are not really that “dark”, it is just they generally prefer to fly at night. The handy chart to the right might help you recognise what is a butterfly and what is a moth.</p>
            <div id="img_container">
      <img src="assets/images/articles/butterflyinqsbg/en/Dayandnight.png" alt="Image" width="400px" >
