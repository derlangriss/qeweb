               <h1>What we get up to in QSBG</h1>
<p><img src="assets/images/articles/butterflyinqsbg/en/behavior.png" alt="Image" width="280px" class="pull-left img-align-left">While admiring me and my friends for our beauty you also have a chance to see us doing all sort of interesting things in our natural surroundings, perhaps as simple as flitting from flower to flower, drinking from streams, mating, laying eggs, or maybe crunching away on a leaf as a caterpillar.<img src="assets/images/articles/butterflyinqsbg/en/protectedspecies.png" alt="Image" width="" class="pull-right img-align-right"></p>

<p>In our diversity and activities we are important parts of a healthy ecosystem. In QSBG one of the most important things we do is pollination of plants (we especially like the red or yellow one and ones with long tubes that suit our very long mouths). Of course, we are an important part of ecotourism too!</p>               
 <div class="clear"></div>
               <h1>Photos only please</h1>
<p>QSBG is part of Doi Pui-Suthep National Park so butterflies and all other living things are protected, please take nothing more than photos (we are very photogenic!). The best time to see and especially photograph butterflies is during the dry season, especially in January, February and March. At this time many species of butterflies concentrate around streams within the garden (see map below). During the wet season the best opportunities are usually around the glasshouse complex, when butterflies visit flowers planted in the gardens in this area.</p>
            
