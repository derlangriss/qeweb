               <h1>Introduction</h1>

<p><img src="assets/images/articles/butterflyinqsbg/en/lifecircle.png" alt="images" width="400" class="pull-right img-align-right">Me and my butterfly friends were described by the French Poet Ponce Denis Ecouchard Lebrun as “flying flowers”. QSBG is a botanic garden and has lots of “flowers chained to the ground” but I’m going to tell you about me and my flying friends.</p>


<p>Butterflies are often admired for their beauty, delicacy and grace, but there is much more to us than this. Butterflies are just one small group among the amazing insects. Many have amazing life-histories and behavior in addition to their beauty. One of the most amazing things about butterflies that anyone can see is their life cycle, and this is how I get to change from a caterpillar to join my friends as a flying flower. Butterflies, and many other insects, have a life cycle where they undergo an amazing change from a simple egg to the feeding machine that is a caterpillar, before finally transforming (inside a chrysalis) to appear as a completely transformed butterfly — looking nothing like the caterpillar it was a few days before.</p>

 <div class="clear"></div>
              <h1>Meet my friends</h1>
<p>Some of the more common butterflies you are likely to see in QSBG include Jezabels (<i>Delias</i>), Grass Yellows (<i>Eurema</i>), and Birdwings (<i>Troides</i>). A simple pamphlet to help you recognize these and other species is “Butterflies of the Garden” which covers 30 species found in QSBG.</p>
 <div id="img_container">
      <img src="assets/images/articles/butterflyinqsbg/en/Euramafixed.png" alt="images" width="550px" >
